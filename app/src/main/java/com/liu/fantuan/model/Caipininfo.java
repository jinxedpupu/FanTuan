package com.liu.fantuan.model;

public class  Caipininfo {

    protected int cpid;
    protected int busid;
    protected String cpname;
    protected int cpjiage;
    protected String cpbeizhu;
    protected String cptupian;

    public int getCpid() {
        return cpid;
    }

    public void setCpid(int cpid) {
        this.cpid = cpid;
    }

    public int getBusid() {
        return busid;
    }

    public void setBusid(int busid) {
        this.busid = busid;
    }

    public String getCpname() {
        return cpname;
    }

    public void setCpname(String cpname) {
        this.cpname = cpname;
    }

    public int getCpjiage() {
        return cpjiage;
    }

    public void setCpjiage(int cpjiage) {
        this.cpjiage = cpjiage;
    }

    public String getCpbeizhu() {
        return cpbeizhu;
    }

    public void setCpbeizhu(String cpbeizhu) {
        this.cpbeizhu = cpbeizhu;
    }

    public String getCptupian() {
        return cptupian;
    }

    public void setCptupian(String cptupian) {
        this.cptupian = cptupian;
    }
}
